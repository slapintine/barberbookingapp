import sqlite3 from "sqlite3";
import path from "path";
import fs from "fs";
import { fileURLToPath } from "url";
import { env } from "./env.js";
import { logger } from "./logger.js";

sqlite3.verbose();

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, "../../");

function createSqliteDb() {
  const resolvedDbPath = path.resolve(projectRoot, env.dbPath);
  const dbDir = path.dirname(resolvedDbPath);

  if (!fs.existsSync(dbDir)) {
    fs.mkdirSync(dbDir, { recursive: true });
  }

  const db = new sqlite3.Database(resolvedDbPath, (err) => {
    if (err) {
      logger.fatal({ err, dbPath: resolvedDbPath }, "Failed to connect to SQLite database");
    } else {
      logger.info({ dbPath: resolvedDbPath }, "SQLite connected");
    }
  });

  db.configure("busyTimeout", 5000);

  db.serialize(() => {
    db.run("PRAGMA foreign_keys = ON");
    db.run("PRAGMA journal_mode = WAL");
  });

  db.client = "sqlite";
  return db;
}

function normalizePostgresSql(sql) {
  let index = 0;
  return String(sql)
    .replace(/INTEGER\s+PRIMARY\s+KEY\s+AUTOINCREMENT/gi, "INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY")
    .replace(/\bREAL\b/gi, "DOUBLE PRECISION")
    .replace(/DEFAULT\s+CURRENT_TIMESTAMP/gi, "DEFAULT NOW()")
    .replace(/datetime\('now'\)/gi, "NOW()")
    .replace(/\?/g, () => {
      index += 1;
      return `$${index}`;
    });
}

function withReturningId(sql) {
  const trimmed = String(sql).trim();
  if (!/^insert\s+into/i.test(trimmed) || /\sreturning\s+/i.test(trimmed)) {
    return sql;
  }
  return `${sql} RETURNING id`;
}

async function createPostgresDb() {
  const { Pool } = await import("pg");
  const pool = new Pool({
    connectionString: env.databaseUrl,
    ssl: env.databaseSsl ? { rejectUnauthorized: false } : undefined,
  });

  pool.on("error", (error) => {
    logger.error({ err: error }, "Unexpected PostgreSQL client error");
  });

  await pool.query("SELECT 1");
  logger.info("PostgreSQL connected");

  const db = {
    client: "postgres",
    pool,
    configure() {},
    serialize(callback) {
      callback();
    },
    run(sql, params = [], callback = () => {}) {
      const queryText = normalizePostgresSql(withReturningId(sql));
      pool
        .query(queryText, params)
        .then((result) => {
          callback.call(
            {
              lastID: result.rows?.[0]?.id,
              changes: result.rowCount,
            },
            null
          );
        })
        .catch((error) => callback(error));
    },
    get(sql, params = [], callback = () => {}) {
      pool
        .query(normalizePostgresSql(sql), params)
        .then((result) => callback(null, result.rows?.[0] || null))
        .catch((error) => callback(error));
    },
    all(sql, params = [], callback = () => {}) {
      pool
        .query(normalizePostgresSql(sql), params)
        .then((result) => callback(null, result.rows || []))
        .catch((error) => callback(error));
    },
    prepare(sql) {
      const pending = [];
      return {
        run(...args) {
          pending.push(
            pool.query(normalizePostgresSql(withReturningId(sql)), args)
          );
        },
        finalize(callback = () => {}) {
          Promise.all(pending)
            .then(() => callback(null))
            .catch((error) => callback(error));
        },
      };
    },
    async transaction(work) {
      const client = await pool.connect();

      const queryClient = {
        run(querySql, params = []) {
          return client
            .query(normalizePostgresSql(withReturningId(querySql)), params)
            .then((result) => ({
              lastID: result.rows?.[0]?.id,
              changes: result.rowCount,
            }));
        },
        get(querySql, params = []) {
          return client
            .query(normalizePostgresSql(querySql), params)
            .then((result) => result.rows?.[0] || null);
        },
        all(querySql, params = []) {
          return client
            .query(normalizePostgresSql(querySql), params)
            .then((result) => result.rows || []);
        },
      };

      try {
        await client.query("BEGIN");
        const result = await work(queryClient);
        await client.query("COMMIT");
        return result;
      } catch (error) {
        await client.query("ROLLBACK").catch(() => {});
        throw error;
      } finally {
        client.release();
      }
    },
    async close() {
      await pool.end();
    },
  };

  return db;
}

const db = env.dbClient === "postgres" ? await createPostgresDb() : createSqliteDb();

export function isPostgres() {
  return db.client === "postgres";
}

export default db;
